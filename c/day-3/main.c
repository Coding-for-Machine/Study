#include<stdio.h>


int main() {
	float a = 10.0;
	float b = 20.0;
	printf("a+b=%f\n", a+b);
	printf("a-b=%f\n", a-b);
	printf("a*b=%f\n", a*b);
	printf("a/b=%.1f\n", a/b);
	// printf("a/.b=%f\n", b%a);
	// prefiks ++ va postfiks ++
	int z = 25;
	printf("z = %d", z++);
	printf("z = %d", ++z);

	return 0;
}
